// Require Google Translate
require_once ('vendor/autoload.php');
use Stichoza\GoogleTranslate\GoogleTranslate;

// Create Google Translate object
$tr = new GoogleTranslate();

// Set the translation "from" and "to"
$tr->setSource('en'); // Translate from English
$tr->setTarget('hi'); // Translate to Hindi

// Fetch all the text nodes and translate
libxml_use_internal_errors(true);
$dom = new DomDocument();
$dom->loadHTML($html, LIBXML_HTML_NOIMPLIED | LIBXML_HTML_NODEFDTD);
$xpath = new DOMXPath($dom);

foreach ($xpath->query('//text()') as $text) {
    if (trim($text->nodeValue)) {
        $text->nodeValue = $tr->translate($text->nodeValue);
    }
}

// Save the translated file
echo $dom->saveHTMLFile('translated.html');
